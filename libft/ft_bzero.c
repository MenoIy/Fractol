#include "libft.h"

void	ft_bzero(void *b, size_t n)
{
	size_t			i;
	unsigned char	*b2;

	b2 = (unsigned char*)b;
	i = 0;
	while (i < n)
	{
		b2[i] = 0;
		i++;
	}
}
