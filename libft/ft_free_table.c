#include "libft.h"

void	ft_free_table(char ***tab)
{
	int i;

	i = 0;
	if ((*tab) == NULL)
		return ;
	while ((*tab)[i])
	{
		free((*tab)[i]);
		(*tab)[i] = NULL;
		i++;
	}
	free(*tab);
	(*tab) = NULL;
}
