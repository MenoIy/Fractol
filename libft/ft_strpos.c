#include "libft.h"

int	ft_strpos(char *str1, char *str2)
{
	int	i;
	int	j;

	if (str2[0] == '\0')
		return (1);
	i = 0;
	while (str1[i])
	{
		j = 0;
		while (str2[j])
		{
			if (str1[i + j] != str2[j])
				break ;
			j++;
		}
		if (str2[j] == '\0')
			return (1);
		i++;
	}
	return (0);
}
