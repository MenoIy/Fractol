#include "libft.h"

size_t	ft_numlen(int n)
{
	unsigned int	nb;
	size_t			len;

	if (n < 0)
	{
		nb = n * -1;
		len = 1;
	}
	if (n > 0)
	{
		len = 0;
		nb = n;
	}
	if (n == 0)
	{
		nb = 0;
		len = 1;
	}
	while (nb >= 1)
	{
		nb = nb / 10;
		len++;
	}
	return (len);
}
